def findScore(arr, num):
    low = 0
    high = len(arr) - 1
    while low <= high:
        mid = (low + high) / 2
        if num == arr[mid]:
            return (mid, mid)
        elif num < arr[mid]:
            high = mid - 1
        else:
            low = mid + 1
    return (high, low)


