def calcScore(ratio, arr):
    (low, high) = findScore(arr, ratio)
    if low == high:
        return values[low + 1]
    else:
        if low == -1:
            return (ratio + 10000) * (100 / (20 * (arr[0] + 10000)))
        elif low == 18:
            return values[18] + (ratio - arr[18]) * (100 / (20 *
                                                            (10000 - arr[18])))
        else:
            return values[low + 1] + (ratio - arr[low]) * (100 / (20 * (
                arr[high] - arr[low])))


